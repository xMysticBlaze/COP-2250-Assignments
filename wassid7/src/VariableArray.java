public class VariableArray {

    // Method to find the maximum value in an array
    public static int maxInArray(int[] array) {
        int max = array[0];
        for (int value : array) {
            if (value > max) {
                max = value;
            }
        }
        return max;
    }

    // Method to find the minimum value in an array
    public static int minInArray(int[] array) {
        int min = array[0];
        for (int value : array) {
            if (value < min) {
                min = value;
            }
        }
        return min;
    }
}
